package dev.yekllurt.interpreter.ast;

public enum ConditionOperator {

    EQUAL,                  // structure: ==
    NOT_EQUAL,              // structure: !=
    GREATER_THAN,           // structure: >
    GREATER_THAN_EQUAL,     // structure: >=
    LESS_THAN,              // structure: <
    LESS_THAN_EQUAL,        // structure: <=
    OR,                     // structure: ||
    AND                     // structure: &&

}

